package ArgentinaPrograma.Clase1;

public class PracticaOperadores {
    public static void main(String[] args) {
        
        int a=5, b=5, c=0, d;
        
        System.out.println("Valor original de la variable a:" + a);
        System.out.println("Valor de a (preincremento)"+ (a=++a*3));
        System.out.println("Valor de b (posincremento)"+ (d=b++*3));
        System.out.println("Variable a: " + a);
        System.out.println("Variable b: " + b);
        System.out.println("Variable d: " + d);
        
        
    }
    
}
